-- create database rdms (result database management system)
CREATE DATABASE rdms;

-- use database created 
USE rdms;

-- create tables
CREATE TABLE students_profile(
	id INT PRIMARY KEY AUTO_INCREMENT,
	student_name VARCHAR(255) NOT NULL,
	current_level INT NOT NULL,
	faculty VARCHAR(255) NOT NULL,
	department VARCHAR(100) NOT NULL,
	last_updated TIMESTAMP DEFAULT NOW()
);

CREATE TABLE courses(
	id INT PRIMARY KEY AUTO_INCREMENT,
	course_code VARCHAR(255) NOT NULL UNIQUE,
	course_description VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE results(
	profile_id INT NOT NULL,
	course_id INT NOT NULL,
	score INT NOT NULL,
	last_updated TIMESTAMP DEFAULT NOW(),
	FOREIGN KEY(profile_id) REFERENCES students_profile(id),
	FOREIGN KEY(course_id) REFERENCES courses(id),
	PRIMARY KEY(profile_id, course_id)
);

